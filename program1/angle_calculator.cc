#include <iostream>
#include <cmath>
using namespace std;

int main() {
  double Angle = 0;
  cout << "Enter the Angle you want to Calculate the trig functions for: ";
  cin >> Angle;
  cout << "Sin: " << sin(Angle) <<endl;
  cout << "Cos: " << cos(Angle) <<endl;
  cout << "Tan: " << tan(Angle) <<endl;
  return 0;
}
